package java_package;
//conditional if else-if block

public class Class0007_ConditionalIfElseIf {
	
	public static void main(String[] args) {
		int number = 25;
		
	    if (number > 50) {
	        System.out.println("Number is greater than 50.");
	    } else if (number > 30) {
	        System.out.println("Number is greater than 30 but less than or equal to 50.");
	    } else if (number > 10) {
	        System.out.println("Number is greater than 10 but less than or equal to 30.");
	    } else {
	        System.out.println("Number is less than or equal to 10.");
	    }

	}

}
