package java_package;
//conditional statements

public class Class0009_ConditionalStatements {
	public static void main(String[] args) {

		int age = 25;
		boolean isStudent = true;

		if (age > 18 && isStudent) {
			System.out.println("You are a student and above 18 years old.");
		} else {
			System.out.println("You are either not a student or below 18 years old.");
		}
	}

}
